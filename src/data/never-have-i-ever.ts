export const ALL_NHIE_PROMPTS: NhiePrompt[] = PROMPTS.map((p, i) => ({
  id: `nhie_${i}`,
  text: p.text,
  packs: p.packs,
}));

export function filterNhiePrompts(
  categories: Category[],
  usedIds: string[],
  familySafe: boolean
): NhiePrompt[] {
  return ALL_NHIE_PROMPTS.filter((p) => {
    if (usedIds.includes(p.id)) return false;
    if (familySafe && !p.packs.includes("family") && p.packs.includes("deep")) {
      // family mode: prefer family + funny only
    }
    if (familySafe) {
      const ok = p.packs.some((pack) => pack === "family" || pack === "funny");
      if (!ok) return false;
    }
    if (categories.length === 0) return true;
    // categories map from packs
    return p.packs.some((pack) => {
      const cat = NHIE_PACK_TO_CATEGORY[pack];
      return categories.includes(cat);
    });
  });
}

export function pickNhiePrompt(
  categories: Category[],
  usedIds: string[],
  familySafe = false
): NhiePrompt {
  let pool = filterNhiePrompts(categories, usedIds, familySafe);
  if (pool.length === 0) {
    pool = filterNhiePrompts(categories, [], familySafe);
  }
  if (pool.length === 0) {
    return pick(ALL_NHIE_PROMPTS);
  }
  return pick(pool);
}
