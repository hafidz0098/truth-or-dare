function ensure200(arr, fillers) {
  const out = [...arr];
  let i = 0;
  while (out.length < 200) {
    out.push(`${fillers[i % fillers.length]} #${Math.floor(i / fillers.length) + 2}`);
    i++;
  }
  return out.slice(0, 200);
}

